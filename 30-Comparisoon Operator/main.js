/*
Comparison Operator
- == Equal
- != Not Equal

- === Identical
- !== Not Identical

- > Larger Than
- >= Larger Than Or Equal

- < Smaler Than
- <= Smaller Than Or Equal
*/

console.log(10 == "10");
console.log(10 != "10");

console.log(10 === "10");
console.log(10 !== "10");

console.log(10 > 20);
console.log(10 > 10);
console.log(10 >= 10);

console.log(10 < 20);
console.log(10 < 10);
console.log(10 <= 10);


